#include "StdAfx.h"
#include "Circle.h"
#define pi (float)22/7

//--------------------------------------------------------------------------------

Circle::Circle(float Radius){
	m_radius = Radius;
}

//--------------------------------------------------------------------------------

float* Circle::Dimension() const{
	float Dim[3] = {m_radius, 0.0, 0.0};
	return Dim;
}

//--------------------------------------------------------------------------------

float Circle::Area() const{
	return (pi*(m_radius*m_radius));
}

//--------------------------------------------------------------------------------

float Circle::Perimeter() const{
	return (pi*(2*m_radius));
}

//--------------------------------------------------------------------------------

char Circle::Identity() const{
	return m_identity;
}

//--------------------------------------------------------------------------------

void Circle::SetUtility(const std::string& Str){
	m_utility = Str;
}

//--------------------------------------------------------------------------------

std::string& Circle::GetUtility(){
	return m_utility;
}

